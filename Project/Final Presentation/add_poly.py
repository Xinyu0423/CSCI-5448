class addTwoNum:
    def _init_(self,x,y):
        self.x=x
        self.y=y

    def add(self):
        result=self.x+self.y
        return result

class addThreeNum:
    def _init_(self,x,y,z):
        self.x=x
        self.y=y
        self.z=z

    def add(self):
        result=self.x+self.y+self.z
        return result